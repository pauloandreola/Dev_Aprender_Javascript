//  Tipo primitivo

const mensagem = 'minha primeira mensagem';

console.log(mensagem);

console.log(typeof mensagem);  // Mostrar que tipo de caracter do tipo string.

console.log(mensagem.length);  // Informa a quantidade de ítens dentro da variável espaço, incluindo os espaços em branco.

console.log(mensagem[4]); // Apresentar somente o quarto caracter. 

console.log(mensagem[5]); // Apresentar somente o quinto caracter Neste caso é um espaço entre as duas palavras 'minha' e 'primeira'. 

console.log(mensagem[8]); // Apresentar somente o nono caracter. 

console.log(mensagem[9]); // Apresentar somente o décimo caracter.